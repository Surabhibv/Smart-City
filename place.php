        <h3>AGUMBE</h3>
        <p>Agumbe is a small village in North Karnataka which receives over 7000 mm of rainfall annually, making it the second rainiest place in India after Cherranpunji.
Agumbe is covered with verdant forests, sparkling streams and little cascades, making the region in the Western Ghats virtually a Garden of Eden.
Sunset view from Agumbe is a scene to behold. Do visit during sunset time and enjoy the captivating moments from the dedicated viewing deck.
Best time to visit is in the post-monsoon and winter months between October and February when Agumbe remains the most scenic and green with mountains covered with dense fog.</p>
