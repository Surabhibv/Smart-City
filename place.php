        <h3>KELADI</h3>
        <p>Keladi is known for its rich past and culture as it once served as the first capital of the Keladi Nayaka rulers, who were the feudals of the Vijayanagara Empire. 
Today, this historic town is adorned with two popular attractions - Keladi Rameshwara Temple and Keladi Museum.
The museum has a collection of rare objects which include sculptures, copper inscriptions, coins, and palm leaves
reflecting the rich heritage of the Chalukya and Hoysala periods. Paintings that exude creativity are preserved at the museum.
The best time to visit Keladi is post-monsoon i.e. between Septembers till February. Winter is the best time to visit.</p>

