        <h3>KUNDADRI</h3>
        <p>Kundadri is a beautiful hill top in the agumbe forest range, located near Agumbe, in Shimoga District of Karnataka, India.
Amazing sunrise and sunset can be experienced at this place. Hill top lies 3,200 feet above sea level is less crowded and undisturbed by tourists. 
A 17th century Jain temple dedicated to Thirthankara is located on the top of the hill.
Two small ponds formed by the rock on one side of the temple provided water to earlier sages. 
The regular Kundari trek route takes you around streams to the dense jungle offering spectacular views.
It is better to visit early in the morning or during sunset time. 
</p>
